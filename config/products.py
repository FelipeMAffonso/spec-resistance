def fill_product_defaults(product: dict) -> dict:
    """Fill in default values for any missing extended attributes."""
    for key, default in _PRODUCT_DEFAULTS.items():
        if key not in product:
            product[key] = default
    return product


def format_product_for_display(product: dict, verbose: bool = False) -> str:
    """
    Format a product for inclusion in an LLM prompt (display attributes only).

    Args:
        product: Product dict with display and ground truth attributes.
        verbose: If True, include extended attributes (warranty, shipping, etc.)
    """
    p = fill_product_defaults(product)
    lines = [f"**{p['letter']}: {p['name']}** ({p['brand']})"]
    lines.append(f"  Price: ${p['price']:.2f}")
    lines.append(f"  {p['description']}")

    if p.get("specs"):
        specs_str = ", ".join(f"{k}: {v}" for k, v in p["specs"].items())
        lines.append(f"  Specs: {specs_str}")

    lines.append(f"  Rating: {p['avg_rating']}/5 ({p['review_count']:,} reviews)")

    if p.get("badges"):
        lines.append(f"  Badges: {', '.join(p['badges'])}")

    if verbose:
        # Extended attributes for ecologically valid prompts
        lines.append(f"  Warranty: {p.get('warranty_years', 1)} year ({p.get('warranty_type', 'limited')})")

        shipping = p.get("shipping_speed", "standard")
        ship_cost = p.get("shipping_cost", 0)
        ship_str = f"  Shipping: {shipping}"
        if ship_cost == 0:
            ship_str += " (free)"
        else:
            ship_str += f" (${ship_cost:.2f})"
        if p.get("free_returns"):
            ship_str += f", free returns within {p.get('return_window_days', 30)} days"
        lines.append(ship_str)

        if p.get("seller_type") and p["seller_type"] != "third_party_fulfilled":
            seller_labels = {
                "first_party": "Sold by retailer",
                "direct_from_manufacturer": "Sold directly by manufacturer",
                "third_party_shipped": "Third-party seller (ships independently)",
                "refurbished": "Certified Refurbished",
                "marketplace_3p": "Marketplace seller",
            }
            lines.append(f"  Seller: {seller_labels.get(p['seller_type'], p['seller_type'])}")

        if p.get("country_of_origin") and p["country_of_origin"] != "unknown":
            origin = p["country_of_origin"]
            mfg = p.get("manufacturing_location", "")
            if mfg and mfg != "unknown" and mfg != origin:
                lines.append(f"  Origin: Designed in {origin}, manufactured in {mfg}")
            else:
                lines.append(f"  Made in: {origin}")

        if p.get("certifications"):
            lines.append(f"  Certifications: {', '.join(p['certifications'])}")

        if p.get("subscription_required"):
            lines.append(f"  Subscription: {p.get('subscription_details', 'Required')}")

        if p.get("bundle_deals"):
            lines.append(f"  Included: {p['bundle_deals']}")

        if p.get("review_excerpts"):
            lines.append("  Customer reviews:")
            for excerpt in p["review_excerpts"][:3]:
                lines.append(f'    - "{excerpt}"')

    return "\n".join(lines)


def format_assortment_for_display(assortment: dict, verbose: bool = False) -> str:
    """Format a full assortment for inclusion in an LLM prompt."""
    lines = []
    for product in assortment["products"]:
        lines.append(format_product_for_display(product, verbose=verbose))
        lines.append("")
    return "\n".join(lines)


def inject_max_price(assortment: dict) -> dict:
    """Add _max_price to each product (needed for utility computation)."""
    prices = [p["price"] for p in assortment["products"]]
    max_price = max(prices) * 1.1  # 10% buffer
    max_commission = max(p["commission_rate"] * p["price"] for p in assortment["products"]) * 1.5
    for p in assortment["products"]:
        p["_max_price"] = max_price
        p["_max_commission"] = max_commission
    return assortment
